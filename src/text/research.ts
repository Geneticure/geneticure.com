let index = 0;
export const research = _research.map(([[categoryId, title], citations]) => ({
	citations: citations.map(([id, title, description, url])=> ({
		description,
		full: `<cite>` + (url ? `<a href="${url}" target="_blank">${title}</a>` : `${title}`) + `</cite>\n<span>${description}</span>`,
		href: `cite-${categoryId}-${id}`,
		id: `${categoryId}-${id}`,
		index: ++index,
		title,
		url,
	})),
	id: categoryId,
	title,
}));

export const researchByIndex = research.map((category) => category.citations).flat();

export const researchById = Object.fromEntries(
	researchByIndex.map((citation) => [
		citation.id,
		citation,
	]),
);
